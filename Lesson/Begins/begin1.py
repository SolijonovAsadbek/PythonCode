# Matematik modullar bilan ishlash uchun 'math' kutubxonasidan foydalanamiz.
import math

# math.sqrt() sonlarni kvadrat ildizdan chiqarish uchun ishlatiladi.
ildiz = math.sqrt(a)
print("Kvatratning tomonini ildizdan chiqarsak: ", ildiz)

# Pythonda sonni darajaga oshirish uchun 'pow()' funksiyasidan foydalanamiz.
yuza = pow(a, 2)  # a^2 (a ning ikkinchi darajasini hissoblash)
print("Kvadratning yuzi: ", yuza)

# Sonni modulldan chiqarish uchun |a-b| qilish uchun 'abs()' funksiyasidan foydalanamiz.
modul = abs(3-4)
print('|3-4| =', modul)

